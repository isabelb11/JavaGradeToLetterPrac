package grade;

public class SwitchCaseStaments {

	public static void main(String[] args) {
		
		String gender = "Female";
		
		switch(gender.toLowerCase()) {
		case "male": System.out.println("gender");
		break;
		
		case "female": System.out.println("gender");
		break;
		default : System.out.println("Wrong value");
		break;
		}
		
		
	}
}
		
		


	

	

